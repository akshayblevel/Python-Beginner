import math
# import math as m (we can use math as well as m)
# from math import sqrt, pow (import specific functions only)


def printvalue():
    print(math.sqrt(25))  # Square Root - 5.0
    print(math.floor(14.6))  # 14
    print(math.ceil(14.2))  # 15
    print(math.pow(5, 3))  # 125.0
    print(math.pi)  # 3.141592653589793
    print(math.e)  # 2.718281828459045


if __name__ == '__main__':
    printvalue()
