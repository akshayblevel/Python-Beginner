# print each value from list
lst = ['Akshay', 42, 'Panth', 6]

for value in lst:
    print(value)

# print each character from string

lst = 'Akshay'

for ch in lst:
    print(ch)

# print range from 1 to 9

for i in range(10):
    print(i)

# print range from 11 to 20 by skipping 2

for i in range(11, 20, 2):
    print(i)

# print range in reverse order

for i in range(20, 11, -1):
    print(i)
