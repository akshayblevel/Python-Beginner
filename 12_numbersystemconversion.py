def printvalue():
    # Decimal | Binary | Octal | Hexadecimal

    print(bin(14))  # 0b1110  0b represents binary and 1110 is the binary number of 14
    print(0b1110)  # 14 converts binary into decimal

    print(oct(14))  # 0o16 0o represents octal and 16 is the octal number of 14

    print(hex(14))  # 0xe 0x represents hexa and e is the hexadecimal number of 14


if __name__ == '__main__':
    printvalue()
